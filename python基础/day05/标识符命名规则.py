# 1、标识符的定义：
# python中的所有的变量、函数名、模块名、方法名、类名、对象所有命名的东西都可以称为标识符。

# 2、标识符的命名规则：
# 组成和变量一样由字母、数字、下划线组成。

# 3、标识符命名规范

# 蛇形命名法：
    # user_name

# 大驼峰命名法：
    # UserName


# 小驼峰命名法：               python用的少
    # userName


# 在python中对于标识符的约定如下：
# 蛇形命名法用于包名、模块名、函数名、方法名、变量名、对象名
# 大驼峰命名用于类名